// QUEM SOMOS
var div = document.getElementById('quemsomos');
var texto = 'QUEM SOMOS? ';

function escrever(str, el) {
  var char = str.split('').reverse();
  var typer = setInterval(function () {
    if (!char.length) return clearInterval(typer);
    var next = char.pop();
    el.innerHTML += next;
  }, );
}

escrever(texto, div);

